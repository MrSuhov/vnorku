def run_command(command, description, check=True):
    """–í—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –∫–æ–º–∞–Ω–¥—ã —Å –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ–º"""
    logger.info(f"üîÑ {description}")
    logger.info(f"–ö–æ–º–∞–Ω–¥–∞: {command}")
    
    try:
        result = subprocess.run(
            command,
            shell=True,
            check=check,
            capture_output=True,
            text=True
        )
        
        if result.returncode == 0:
            logger.info(f"‚úÖ {description} - —É—Å–ø–µ—à–Ω–æ")
            if result.stdout.strip():
                logger.info(f"Output: {result.stdout.strip()}")
        else:
            logger.error(f"‚ùå {description} - –æ—à–∏–±–∫–∞")
            if result.stderr.strip():
                logger.error(f"Error: {result.stderr.strip()}")
            
        return result.returncode == 0
        
    except subprocess.CalledProcessError as e:
        logger.error(f"‚ùå {description} - –æ—à–∏–±–∫–∞: {e}")
        return False
    except Exception as e:
        logger.error(f"‚ùå –ù–µ–æ–∂–∏–¥–∞–Ω–Ω–∞—è –æ—à–∏–±–∫–∞ –≤ {description}: {e}")
        return False


